alphabet = input('Enter in the alphabet to check for >>> ')
if alphabet == "a" or alphabet == "e" or \
    alphabet == "i" or alphabet == "o" or \
    alphabet == "u":
    print("It's a Vowel")
elif alphabet == "y":
    print("It's sometimes a Vowel and sometimes a Consonant")
else:
    print("It's a Consonant")
